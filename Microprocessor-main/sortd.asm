.model small
.386
.data
ARRAY DW 20 DUP (?)
DATA1 DW 0000H
NUMB DW 0000H

msg1 db 10, 13, "Enter the size of the array: $"
msg2 db 10, 13, "Enter the array: $"
msg3 db 10, 13, "The sorted array is: $"

.code
.startup
MOV AH, 09
MOV DX, OFFSET msg1
INT 21H

; INPUT THE SIZE OF THE ARRAY
MOV AH, 01
INT 21H
SUB AL, 30H
MOV AH, 00
MOV CX, AX
MOV DATA1, AX

MOV AH, 09
MOV DX, OFFSET msg2
INT 21H

; INPUT THE ARRAY
MOV SI, 00
MOV BX, OFFSET ARRAY
L1: MOV AH, 02H
MOV DL, 0AH ; jump onto next line
INT 21H
MOV AH, 01H ; input a digit
INT 21H
SUB AL, 30H
MOV AH, 00H
MOV [BX + SI], AX
INC SI
LOOP L1

; SORT THE ARRAY
MOV CX, DATA1
MOV BX, OFFSET ARRAY
MOV DI, CX
L2: MOV CX, DATA1
MOV NUMB, CX
DEC NUMB
MOV CX, NUMB
MOV SI, 00
L3: MOV AL, [BX + SI]
CMP AL, [BX + SI + 1]
JA L4
XCHG AL, [BX + SI + 1]
MOV [BX + SI], AL
L4: INC SI
LOOP L3
DEC DI
JNZ L2

MOV AH, 09
MOV DX, OFFSET msg3
INT 21H

; PRINT THE ARRAY
MOV CX, DATA1
MOV SI, 00
MOV BX, OFFSET ARRAY
L5: MOV AH, 02H
MOV DL, 0AH
INT 21H
MOV DX, [BX + SI]
ADD DL, 30H
INT 21H
INC SI
LOOP L5

.EXIT
END